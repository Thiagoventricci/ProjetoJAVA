package dados;

public class Agencia {
    private int cod_agencia;
    private String num_agencia;
    private String nome_agencia;
    
    public int getCod_agencia() {
        return cod_agencia;
    }
    public void setCod_agencia(int cod_agencia) {
        this.cod_agencia = cod_agencia;
    }
    public String getNum_agencia() {
        return num_agencia;
    }
    public void setNum_agencia(String num_agencia) {
        this.num_agencia = num_agencia;
    }
    public String getNome_agencia() {
        return nome_agencia;
    }
    public void setNome_agencia(String nome_agencia) {
        this.nome_agencia = nome_agencia;
    }
    
   

    
}
